CREATE TABLE   USER_INFO  
   (	 USER_NO  NUMBER, 
	 USER_EMAIL  VARCHAR2(100 BYTE), 
	 USER_Password  VARCHAR2(100 BYTE), 
	 USER_NICK  VARCHAR2(100 BYTE), 
	 USER_BIRTH  VARCHAR2(100 BYTE), 
	 USER_PHone  VARCHAR2(100 BYTE), 
	 USER_ADDRESS  VARCHAR2(100 BYTE), 
	 USER_ADDRESS_DETAIL  VARCHAR2(50 BYTE), 
	 USER_JOIN_DATE  DATE
   ) 

CREATE SEQUENCE user_info_seq
START WITH 1
INCREMENT BY 1;